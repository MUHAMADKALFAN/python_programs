from tkinter import *
w=Tk()
w.config(background='black')
w.geometry('300x300')
l1=Label(w,text="Number1")
e1=Entry(w,width=20)
l2=Label(w,text="Number2")
e2=Entry(w,width=20)
l1.place(x=10,y=20)
l2.place(x=10,y=50)
e1.place(x=100,y=20)
e2.place(x=100,y=50)
def add():
    r=(int(e1.get())+int(e2.get()))
    e3.delete(0,END)
    e3.insert(END,r)
def mul():
    r=(int(e1.get())*int(e2.get()))
    e3.delete(0,END)
    e3.insert(END,r)
def sub():
    r=(int(e1.get())-int(e2.get()))
    e3.delete(0,END)
    e3.insert(END,r)
def div():
    r=(int(e1.get())/int(e2.get()))
    e3.delete(0,END)
    e3.insert(END,r)
